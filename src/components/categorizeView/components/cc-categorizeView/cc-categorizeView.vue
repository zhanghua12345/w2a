<template>
  <view class="content">
    <scroll-view scroll-y class="left-aside">
      <view
        v-for="item in flist"
        :key="item.id"
        class="f-item b-b"
        :class="{ active: item.id === currentId }"
        @click="tabtap(item)"
      >
        {{ item.name }}
      </view>
    </scroll-view>
    <scroll-view
      scroll-with-animation
      scroll-y
      class="right-aside"
      :scroll-top="tabScrollTop"
      @scroll="asideScroll"
    >
      <view
        v-for="item in slist"
        :id="'main-' + item.id"
        :key="item.id"
        class="s-list"
      >
        <text class="s-item">{{ item.name }}</text>
        <view class="t-list">
          <!-- v-if改v-show适配vue3 -->
          <view
            v-for="titem in tlist"
            v-show="titem.pid === item.id"
            :key="titem.id"
            class="t-item"
            @click="navToList(titem)"
          >
            <image :src="titem.picture" />
            <text>{{ titem.name }}</text>
          </view>
        </view>
      </view>
    </scroll-view>
  </view>
</template>

<script>
export default {
  props: {
    // 第一级数组
    flist: {
      type: Array,
      default: [],
    },
    // 第二级数组
    slist: {
      type: Array,
      default: [],
    },
    // 第三级数组
    tlist: {
      type: Array,
      default: [],
    },
  },
  data() {
    return {
      sizeCalcState: false,
      tabScrollTop: 0,
      currentId: 1,
    }
  },

  methods: {
    //一级分类点击
    tabtap(item) {
      if (!this.sizeCalcState) {
        this.calcSize()
      }

      this.currentId = item.id
      const index = this.slist.findIndex((sitem) => sitem.pid === item.id)
      this.tabScrollTop = this.slist[index].top
    },
    //右侧栏滚动
    asideScroll(e) {
      if (!this.sizeCalcState) {
        this.calcSize()
      }
      const scrollTop = e.detail.scrollTop
      const tabs = this.slist.filter((item) => item.top <= scrollTop).reverse()
      if (tabs.length > 0) {
        this.currentId = tabs[0].pid
      }
    },
    //计算右侧栏每个tab的高度等信息
    calcSize() {
      let h = 0
      this.slist.forEach((item) => {
        const view = uni.createSelectorQuery().select(`#main-${item.id}`)
        view
          .fields(
            {
              size: true,
            },
            (data) => {
              item.top = h
              h += data.height
              item.bottom = h
            }
          )
          .exec()
      })
      this.sizeCalcState = true
    },
    navToList(item) {
      this.$emit('itemClick', item)
    },
  },
}
</script>

